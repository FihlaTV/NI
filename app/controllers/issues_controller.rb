    # @issues = Issue.search(params)
    # TOFIX: TODO: Search + pagination?
    # @issues = Issue.order("release").reverse_order.page(params[:page]).per(2).search(params)

    @issues = Issue.search(params, current_user.try(:admin?))

    # Set meta tags
    set_meta_tags :title => "Magazine archive",
                  :description => "An archive of all the New Internationalist magazines available as digital editions.",
                  :keywords => "new, internationalist, magazine, archive, digital, edition",
                  :open_graph => {
                    :title => "Magazine archive",
                    :description => "An archive of all the New Internationalist magazines available as digital editions.",
                    #:type  => :magazine,
                    :url   => issues_url,
                    :image => @issues.sort_by{|i| i.release}.last.try(:cover_url, :thumb2x).to_s,
                    :site_name => "New Internationalist Magazine Digital Edition"
                  }

    respond_to do |format|
      format.html # index.html.erb
      format.json { render json: @issues }
    end
  end

  # GET /issues/1
  # GET /issues/1.json
  def show
    # @issue = Issue.find(params[:id])
    @all_articles = @issue.articles
    @keynote = @issue.articles.find_by_keynote(true)
    @features = @issue.articles_of_category("/features/")
    @agendas = @issue.articles_of_category("/sections/agenda/")
    @arguments = @issue.articles_of_category("/argument/")
    @alternatives = @issue.articles_of_category("/sections/alternatives/")
    @columns = @issue.articles_of_category("/columns/")
    @blogs = @issue.articles_of_category("/blog/")
    @uncategorised = @all_articles - [@keynote] - @features - @agendas - @arguments - @columns - @blogs - @alternatives

    # Set meta tags
    set_meta_tags :title => @issue.title,
                  :description => "Read the #{@issue.release.strftime("%B, %Y")} digital edition of the New Internationalist magazine - #{@issue.title}",
                  :keywords => "new, internationalist, magazine, digital, edition, #{@issue.title}",
                  :open_graph => {
                    :title => @issue.title,
                    :description => "Read the #{@issue.release.strftime("%B, %Y")} digital edition of the New Internationalist magazine - #{@issue.title}",
                    #:type  => :magazine,
                    :url   => issue_url(@issue),
                    :image => @issue.cover_url(:thumb2x).to_s,
                    :site_name => "New Internationalist Magazine Digital Edition"
                  }

    respond_to do |format|
      format.html # show.html.erb
      format.json { render json: @issue }
    end
  end

